package dasar;


/*
 *  1. Struktur Dasar Program Java :
 *      a. public class Main: Mendeklarasikan sebuah kelas bernama Main.
 *      b. public static void main(String[] args): Titik awal eksekusi program.
 *      c. System.out.println("Hello, World!");: Menampilkan teks ke konsol.
 */

 public class Main { // public class Main: Mendeklarasikan sebuah kelas bernama Main.
    public static void main(String[] args) { // public static void main(String[] args): Titik awal eksekusi program.

        System.out.println("Hello world"); // System.out.println("Hello, World!");: Menampilkan teks ke konsol.
    }

}
